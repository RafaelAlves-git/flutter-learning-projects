import 'dart:math';

void main() {
  int a = 10;
  int b = 20;
  int c = a + b;
  int d = 0;

  print(a);
  print(b);
  print(c);
  print(d);

  print("valor de a: $a");
  print("valor de b: $b");
  print("valor de c: $c");
  print("valor de d: $d");

  print("O dobro de a é: ${a * 2}");
  print("O dobro de b é: ${b * 2}");
  print("O dobro de c é: ${c * 2}");

  print("O triplo de a é: ${a * 3}");
  print("O triplo de b é: ${b * 3}");
  print("O triplo de c é: ${c * 3}");

  print("A metade de a é: ${a / 2}");
  print("A metade de b é: ${b / 2}");
  print("A metade de c é: ${c / 2}");

  print("A raiz quadrada de a é: ${sqrt(a)}");
  print("A raiz quadrada de b é: ${sqrt(b)}");
  print("A raiz quadrada de c é: ${sqrt(c)}");
}
